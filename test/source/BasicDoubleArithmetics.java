class BasicDoubleArithmetics {


    public double double_addition(double doublep_1, double doublep_2) {
        return doublep_1 + doublep_2;
    }

    public double double_substraction(double doublep_1, double doublep_2) {
        return doublep_1 - doublep_2;
    }

    public double double_multiplication(double doublep_1, double doublep_2) {
        return doublep_1 * doublep_2;
    }

    public double double_division(double doublep_1, double doublep_2) {
        return doublep_1 / doublep_2;
    }

}
