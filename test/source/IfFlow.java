class IfFlow {


    public int int_eq(int intp_1, int intp_2) {
        if (intp_1 == intp_2){
            return 1;
        }
        return 0;
    }

    public int int_lt(int intp_1, int intp_2) {
        if (intp_1 < intp_2){
            return 1;
        }
        return 0;
    }

    public int int_le(int intp_1, int intp_2) {
        if (intp_1 <= intp_2){
            return 1;
        }
        return 0;
    }

    public int int_gt(int intp_1, int intp_2) {
        if (intp_1 > intp_2){
            return 1;
        }
        return 0;
    }

    public int int_ge(int intp_1, int intp_2) {
        if (intp_1 >= intp_2){
            return 1;
        }
        return 0;
    }

}
