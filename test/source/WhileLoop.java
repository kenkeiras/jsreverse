class WhileLoop {


    public void while_eq(int intp_1, int intp_2) {
        while (intp_1 == intp_2){
            intp_1++;
        }
    }

    public void int_lt(int intp_1, int intp_2) {
        while (intp_1 < intp_2){
            intp_1++;
        }
    }

    public void int_le(int intp_1, int intp_2) {
        while (intp_1 <= intp_2){
            intp_1++;
        }
    }

    public void int_gt(int intp_1, int intp_2) {
        while (intp_1 > intp_2){
            intp_1--;
        }
    }

    public void int_ge(int intp_1, int intp_2) {
        while (intp_1 >= intp_2){
            intp_1--;
        }
    }

}
