class BasicLongArithmetics {


    public long long_addition(long longp_1, long longp_2) {
        return longp_1 + longp_2;
    }

    public long long_substraction(long longp_1, long longp_2) {
        return longp_1 - longp_2;
    }

    public long long_multiplication(long longp_1, long longp_2) {
        return longp_1 * longp_2;
    }

    public long long_division(long longp_1, long longp_2) {
        return longp_1 / longp_2;
    }

}
