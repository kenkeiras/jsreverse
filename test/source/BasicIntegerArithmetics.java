class BasicIntegerArithmetics {


    public int integer_addition(int intp_1, int intp_2) {
        return intp_1 + intp_2;
    }

    public int integer_substraction(int intp_1, int intp_2) {
        return intp_1 - intp_2;
    }

    public int integer_multiplication(int intp_1, int intp_2) {
        return intp_1 * intp_2;
    }

    public int integer_division(int intp_1, int intp_2) {
        return intp_1 / intp_2;
    }

}
