class BasicFloatArithmetics {


    public float float_addition(float floatp_1, float floatp_2) {
        return floatp_1 + floatp_2;
    }

    public float float_substraction(float floatp_1, float floatp_2) {
        return floatp_1 - floatp_2;
    }

    public float float_multiplication(float floatp_1, float floatp_2) {
        return floatp_1 * floatp_2;
    }

    public float float_division(float floatp_1, float floatp_2) {
        return floatp_1 / floatp_2;
    }

}
